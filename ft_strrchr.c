#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	len;

	if ((char)c == '\0')
		return ((char *)(s + ft_strlen(s)));
	len = ft_strlen(s) - 1;
	while (len >= 0)
	{
		if (s[len] == (char)c)
			return ((char *)(s + len));
		len--;
	}
	return (NULL);
}
